        private static void ShowData(bool[,] dt)
        {
            int dots = 0;
            StringBuilder disp = new StringBuilder("");
            for (int x = 0; x <= dt.GetUpperBound(0); x++)
            {
                for (int y = 0; y <= dt.GetUpperBound(1); y++)
                {
                    if (dt[x, y])
                    {
                        dots++;
                        disp.Append("X");
                    }
                    else
                    {
                        disp.Append(" ");
                    }
                }
                disp.AppendLine();
            }
            Console.WriteLine(disp);
            Console.WriteLine(dots);
        }

